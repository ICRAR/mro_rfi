"""
The database to hold the data
"""
from sqlalchemy import BigInteger, Column, DateTime, Float, MetaData, String, Table

METADATA = MetaData(
    naming_convention={
        "ix": 'ix_%(column_0_label)s',
        "uq": "uq_%(table_name)s_%(column_0_name)s",
        "ck": "ck_%(table_name)s_%(constraint_name)s",
        "fk": "fk_%(table_name)s_%(column_0_name)s_%(referred_table_name)s",
        "pk": "pk_%(table_name)s"
    }
)


RAW_TRAIN_DATA = Table(
    'raw_train_data',
    METADATA,
    Column('data_id', BigInteger, primary_key=True, autoincrement=True),
    Column('date', DateTime, nullable=False),
    Column('column_2', String(128), nullable=False),
    Column('latitude', Float, nullable=False),
    Column('longitude', Float, nullable=False),
)
